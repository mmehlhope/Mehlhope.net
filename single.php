                <h2>Responsible Web Design</h2>
    
                <section class="fullArticle">
                    <p>As web designers and developers, we carry the heavy burden of ensuring that content is delivered to an incredibly heterogenius audience – internet-enabled devices. These devices come in different shapes, sizes, and capacities. Regardless of their capabilities each device platform deserves the same level of respect, something that we have not historically given each equally. While responsive and adaptive web design have become buzz words in the industry, they are simply emblematic of our need to design responsibly — for all users and devices — rather than for the capable few.</p>
                    <p>Many have said that following certain web design methodologies is indeed responsible web design, but Responsible Web Design is deserving of its own definition and properties.</p>
                    <p>Like the entrance to a public or civic building, the web’s content deserves special considerations in its deployment to the world and should adhere to certain principles:</p>
                    <ol>
                        <li>It must be accessible.</li>
                        <li>It must deliver what is expected of it.</li>
                        <li>It must be efficient.</li>
                        <li>It must be safe.</li>
                    </ol>
                    <p>These principles are self-evident and while the web has not followed them, many other facets of our world do.</p>
                    <p>As an example, if a contractor irresponsibly designed a civic building by making the steps too steep, not installing proper railing, or lacking wheelchair accessibility, his design would be protested by the public and he would probably have a hard time finding jobs in the future. On the web, we make equally irresponsbile design decisions daily that are followed by an outcry from the public yet we ignore them and keep our jobs — this is unacceptable.</p>
                    <p>A user may need to book an emergency flight to see a dying relative or research what medications interact with a prescription they are on, but oftentimes poor content design makes it inaccessible to them. By designing irresponsbily, we are actively denying people information that in some cases may be equally as critical as access to a civic building.</p>
                    <p>While initially it may take longer and potentially cost more for us to make Responsible Web Design the standard, once it has become the status quo it will simply be a part of the process. No one creating a civic building in the modern day says, “Well, we could make this cheaper and build it faster if we just ignored the children or handicapped people and used sub-grade building materials.” Similarly, we should never take the faster/cheaper approach for building a website unless a very specific case allows for it. Much like the government official making considerations for a civic building, we are public servants making considerations for the assessibility of information on the web.</p>
                    <p>Moving forward, it is our obligation to create and share the best practices for Responsible Web Design so that we may all shepherd in a new era of accessible, device-agnostic information.</p>
                    <p>What do you think?</p>
                    <blockquote>
                        <p>Note: Given this insight, I am in the process of completely re-coding my own site to adhere to these standards. It is a fantastic opportunity to explore the technical aspect, but also provide a simple example.</p>
                    </blockquote>
